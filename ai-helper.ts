// تصنيفات الأسئلة المعتمدة بالمنظومة
export enum MessageCategory {
  PHYSICS = "physics",                 // سؤال فيزياء
  MATH = "math",                       // سؤال رياضيات
  GENERAL = "general",                 // سؤال عام (علوم أخرى، تنظيم دراسي، كيمياء/أحياء)
  GREETING = "greeting",               // سؤال ترحيب وتعريف بالاستاذ سياف والمنصة
  OUT_OF_SCOPE = "out_of_scope",       // سؤال خارج عن اختصاص التعليم والمنصة
  INCOMPREHENSIBLE = "incomprehensible" // سؤال غير واضح أو حروف عشوائية
}

// واجهة تفصيلية لنتائج التحليل
export interface MessageAnalysis {
  category: MessageCategory;
  confidence: number;      // نسبة اليقين والفهم (من 0 إلى 100)
  normalizedMessage: string;
  isLongMessage: boolean;  // هل السؤال طويل ومحتاج تلخيص؟
  primaryTerm: string;     // الكلمة الأكثر أهمية بالمدخلات
}

/**
 * تنظيف وتقطيع وتقييس وتحليل النصوص العربية لدعم معالجة الجمل وليس الكلمات فقط
 */
export function normalizeArabic(text: string): string {
  if (!text) return "";
  let clean = text.trim().toLowerCase();
  
  // إزالة علامات الترقيم الشائعة والرموز المشتتة
  clean = clean.replace(/[?.!,،;؛:()"'“”]/g, " ");
  
  // إزالة التشكيل العربي بالكامل لتوحيد اللفظ
  clean = clean.replace(/[\u064B-\u065F]/g, "");
  
  // توحيد الألفات والهمزات لتسهيل المطابقة الدقيقة
  clean = clean.replace(/[أإآ]/g, "ا");
  clean = clean.replace(/ة/g, "ه");
  clean = clean.replace(/ى/g, "ي");
  
  // استبدال المسافات المتعددة بمسافة واحدة
  clean = clean.replace(/\s+/g, " ");
  return clean;
}

/**
 * التحقق من وجود حروف مكررة بشكل عبثي أو كلمات لا يمكن قراءتها (Gibberish)
 */
export function isGibberish(text: string): boolean {
  const normalized = text.trim();
  if (normalized.length === 0) return true;
  
  // فحص تكرار نفس الحرف أكثر من 4 مرات متتالية (مثال: سسسسسس، ههههههه، ذذذذ)
  if (/(.)\1{4,}/.test(normalized)) return true;
  
  // فحص تكرار أحرف متناوبة لا معنى لها أو استخدام حروف إنجليزية عشوائية غير مقروءة بطلب عربي
  if (/^[a-z]{5,}$/i.test(normalized) && !normalized.includes("physics") && !normalized.includes("math")) return true;
  
  // إذا كان النص لا يحتوي على أي حرف عربي أو إنجليزي مقروء ولكنه يحتوي رموزاً عبثية فقط
  const absoluteSymbols = normalized.replace(/[a-zA-Z\u0600-\u06FF0-9\s]/g, "");
  if (absoluteSymbols.length / normalized.length > 0.4) return true;
  
  return false;
}

/**
 * تصنيف السؤال وحساب مستوى الثقة والتحليل السياقي للمدخلات
 */
export function analyzeMessage(message: string): MessageAnalysis {
  const normalized = normalizeArabic(message);
  const wordsCount = message.trim().split(/\s+/).length;
  const charLength = message.trim().length;
  
  const analysis: MessageAnalysis = {
    category: MessageCategory.INCOMPREHENSIBLE,
    confidence: 100,
    normalizedMessage: normalized,
    isLongMessage: charLength > 150 || wordsCount > 25,
    primaryTerm: ""
  };

  // 1. فحص النصوص غير المفهومة أو الفارغة أو القصيرة جداً دون معنى واضح
  if (charLength === 0) {
    analysis.category = MessageCategory.INCOMPREHENSIBLE;
    analysis.confidence = 100;
    return analysis;
  }

  if (isGibberish(message)) {
    analysis.category = MessageCategory.INCOMPREHENSIBLE;
    analysis.confidence = 95;
    return analysis;
  }

  // 2. قوائم المعاني والسياقات لتمييز الجمل والتحليل الشامل
  const greetingKeywords = [
    "مرحبا", "اهلا", "السلام", "سلام عليكم", "صباح الخير", "مساء الخير", 
    "هلا", "شلونك", "منور", "يسعد مساك", "مرحبتين", "كيف الحال", "كيف حالك", "بوت", "من انت",
    "سياف", "الشباطي", "المنصه", "المرجع", "مرجع"
  ];

  const mathKeywords = [
    "رياضيات", "حساب", "تفاضل", "تكامل", "جبر", "مصفوفات", "لوغاريتم", "معادله تربيعيه", 
    "تكاملات", "اشتقاق", "زاوية", "المثلثات", "جا وجتا", "جيب وجيب تمام", "كسور", "احسب",
    "قسمه وضرب", "جذر", "معادلات رياضية", "مجهول"
  ];

  const generalScienceKeywords = [
    "كيف اذاكر", "نصيحه للمذاكره", "امتحان", "اختبار", "طريقة الدراسة", "تنظيم الوقت", 
    "كيمياء", "احياء", "تفاعل", "عنصر", "رابطه", "دنا", "dna", "جين", "خليه", "انقسام خلوي",
    "الجدول الدوري", "ثانوية عامة", "دراسة", "مذاكرة", "وزارة"
  ];

  const outOfScopeKeywords = [
    "طبخ", "طبخه", "اكل", "طعام", "بيض", "كبسه", "رز", "شاورما", "برجر", "مطبخ",
    "كوره", "كره القدم", "مدريد", "برشلونه", "ميسي", "رونالدو", "المنتخب", "الدوري",
    "ببجي", "العاب فيديو", "روبلوكس", "بلايستيشن", "جيمينج", "تيك توك", "سناب", "انستقرام",
    "فيلم", "مسلسل", "موسيقى", "اغنيه", "حب", "غرام", "شعر غزل", "سياسة", "حكومة", "حرب",
    "اسعار الصرف", "دولار", "سعودي اليمن", "واتساب", "تليجرام", "فيس بوك"
  ];

  const physicsKeywords = [
    "قصور ذاتي", "قوة", "طاقه", "حركه", "سرعه", "تسارع", "جاذبيه", "سقوط حر", "نيوتن", 
    "كتله", "وزن", "متجهات", "موجة", "صوت", "ضوء", "كهرباء", "مغناطيس", "مجال", "حث", 
    "فاراداي", "كهرومغناطيسي", "النسبية", "اينشتاين", "كم", "كوانتم", "انكسار", "انعكاس", 
    "حيود", "تداخل", "ضغط", "طفو", "ارخميدس", "هوكس", "الشد السطحي", "الخاصية الشعرية", 
    "محاكاة", "تجربة", "مختبر"
  ];

  // 3. التحقق من تطابق الجمل أو المركبات السياقية لمنع التداخل (Context Check)
  // مثال: إذا قال "قوة الإرادة" أو "طاقة إيجابية" فهذا ليس فيزياء!
  let isColloquialNonScience = false;
  if (normalized.includes("قوة الشخصية") || normalized.includes("قوة الارادة") || normalized.includes("قوة ايمان")) {
    isColloquialNonScience = true;
  }
  if (normalized.includes("طاقة ايجابية") || normalized.includes("طاقة سلبية") || normalized.includes("طاقة روحية")) {
    isColloquialNonScience = true;
  }
  if (normalized.includes("حركة سياسية") || normalized.includes("حركة تمرد") || normalized.includes("حركة الجيش")) {
    isColloquialNonScience = true;
  }

  // 4. احتساب الأوزان والمطابقة
  let greetingScore = 0;
  let mathScore = 0;
  let generalScore = 0;
  let outOfScopeScore = 0;
  let physicsScore = 0;

  // فحص ترحيبي عالي الأولوية
  greetingKeywords.forEach(k => {
    if (normalized.includes(k)) greetingScore += normalized.includes("الاستاذ سياف") || normalized.includes("الشباطي") ? 3 : 1.5;
  });

  // فحص الرياضيات
  mathKeywords.forEach(k => {
    if (normalized.includes(k)) mathScore += 2;
  });

  // فحص عام
  generalScienceKeywords.forEach(k => {
    if (normalized.includes(k)) generalScore += 2;
  });

  // فحص خارج النطاق
  outOfScopeKeywords.forEach(k => {
    if (normalized.includes(k)) outOfScopeScore += 3;
  });

  // فحص الفيزياء (مع استبعاد الاستخدام الشفهي غير الفيزيائي)
  if (!isColloquialNonScience) {
    physicsKeywords.forEach(k => {
      if (normalized.includes(k)) physicsScore += 2;
    });
  } else {
    outOfScopeScore += 2; // يضاف إلى خارج النطاق إذا وجدنا استعارات لغوية غير علمية
  }

  // 5. اتخاذ القرار استناداً إلى الأعلى نقاطاً
  const maxScore = Math.max(greetingScore, mathScore, generalScore, outOfScopeScore, physicsScore);

  if (maxScore === 0) {
    // لم يتم مطابقة أي كلمة مفتاحية، إذا كانت الرسالة قصيرة جداً (كلمة عشوائية وحدة وغير مسجلة كشيء علمي)
    if (wordsCount <= 2) {
      analysis.category = MessageCategory.INCOMPREHENSIBLE;
      analysis.confidence = 35; // ثقة منخفضة تطلب إعادة صياغة
    } else {
      // إذا كان السؤال معقول الطول ولكنه لا يحتوي على كلمات فيزياء أو رياضيات أو ترحيب، نصنفه كخارج المنهج والمنصة
      analysis.category = MessageCategory.OUT_OF_SCOPE;
      analysis.confidence = 65;
    }
  } else if (maxScore === greetingScore) {
    analysis.category = MessageCategory.GREETING;
    analysis.confidence = Math.min(100, 70 + greetingScore * 10);
  } else if (maxScore === outOfScopeScore) {
    analysis.category = MessageCategory.OUT_OF_SCOPE;
    analysis.confidence = Math.min(100, 75 + outOfScopeScore * 8);
  } else if (maxScore === mathScore) {
    analysis.category = MessageCategory.MATH;
    analysis.confidence = Math.min(100, 70 + mathScore * 10);
  } else if (maxScore === generalScore) {
    analysis.category = MessageCategory.GENERAL;
    analysis.confidence = Math.min(100, 70 + generalScore * 10);
  } else {
    analysis.category = MessageCategory.PHYSICS;
    analysis.confidence = Math.min(100, 70 + physicsScore * 10);
  }

  // 6. استخلاص الكلمة الأساسية للتلخيص المعرفي الذاتي
  const stopwords = [
    "ماهو", "ماهي", "ماذا", "معنى", "تعريف", "اشرح", "كيف", "لماذا", "الذي", "التي", 
    "سؤال", "استفسار", "اريد", "حل", "مفهوم", "فيزياء", "فيزيائي", "الفيزياء", "المنهج", 
    "عن", "على", "في", "من", "هل", "ما", "يا", "استاذ", "سياف", "الشباطي", "ممكن", "شرح", "بالتفصيل"
  ];
  
  const terms = message.split(/\s+/).filter(w => {
    const cleanWord = normalizeArabic(w);
    return w.length > 2 && !stopwords.includes(w) && !stopwords.includes(cleanWord);
  });
  
  analysis.primaryTerm = terms.length > 0 ? terms.slice(0, 3).join(" ") : "";

  return analysis;
}

/**
 * دالة التلخيص التلقائي والذكي للأسئلة المركبة والطويلة قبل بناء المحتوى (تحسين الردود الشاملة)
 */
export function summarizeQuestionLocally(message: string, analysis: MessageAnalysis): string {
  if (!analysis.primaryTerm) return "طلب شرح تفصيلي واستفسار علمي";
  
  if (analysis.category === MessageCategory.PHYSICS) {
    return `فهم المفهوم الفيزيائي ومناقشة تفاصيل: [${analysis.primaryTerm}]`;
  } else if (analysis.category === MessageCategory.MATH) {
    return `تحليل الخطوات الرياضية وتفسير العمليات بخصوص: [${analysis.primaryTerm}]`;
  } else if (analysis.category === MessageCategory.GENERAL) {
    return `استفسار دراسي ومنهجي عام بخصوص موضوع: [${analysis.primaryTerm}]`;
  }
  return `الاستفسار عن: [${analysis.primaryTerm}]`;
}

